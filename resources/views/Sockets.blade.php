                        <h4>Sockets.</h4>
                        <p>
                           Los sockets son mecanismos de comunicación entre procesos que permiten que un proceso hable (emita o reciba información ) con otro proceso incluso estando en distintas máquinas.
                           Una forma de conseguir que dos programas se transmitan datos. 
                           <br>Un socket no es más que un "canal de comunicación" entre dos programas que corren sobre ordenadores distintos o incluso en el mismo ordenador. 
                           Desde el punto de vista de programación, un socket no es más que un "fichero" que se abre de una manera especial.                       
                        </p>
                     </div>
                  </div>
               
                  <div class="col-md-6">
                     <div class="full blog_cont">
                        <h4>Proceso del uso de sockets en aplicaciones Cliente/Servidor.</h4>
                        <p>
                           Comunicación cliente/servidor con Socket TCP
                        <br>El interfaz que da soporte a sockets TCP está constituida por las clases ServerSocket y Socket
                        <br>1.	ServerSocket: es utilizada por un servidor para crear un socket en el puerto en el que escucha las peticiones de conexión de los clientes. Su método accept toma una petición de conexión de la cola, o si la cola está vacía, se bloquea hasta que llega una petición. El resultado de ejecutar accept es una instancia de Socket, a través del cual el servidor tiene acceso a los datos enviados por el cliente.
                        </p>
                     </div>
                  </div>
                  <div class="col-md-6">
                     <img src="images/SOCKET4.jpg" alt="#" />
                  </div>
                        <p>
                           <br>2. Socket: es utilizada tanto por el cliente como por el servidor. El cliente crea un socket especificando el nombre DNS del host y el puerto del servidor, así se crea el socket local y además se conecta con el servicio.
                        Esta clase proporciona los métodos getInputStream y getOutputStream para acceder a los dos streams asociados a un socket (recordemos que son bidireccionales), y devuelve tipos de datos InputStream y OutputStream, respectivamente, a partir de los cuales podemos construir BufferedReader y PrintWriter, respectivamente, para poder procesar los datos de forma más sencilla.
                        </p>
                  <div class="col-md-6">
                     <img src="images/Sockets5.jpg" alt="#" />
                  </div>
                  <div class="col-md-6">
                     <div class="full blog_cont">
                        <p>
                           Si nos centramos en la parte de comunicaciones, la forma general de implementar un cliente será
                        <br>1.	Crear un objeto de la clase Socket, indicando host y puerto donde corre el servicio.
                        <br>2.	Obtener las referencias al stream de entrada y al de salida al socket.
                        <br>3.	Leer desde y escribir en el stream de acuerdo al protocolo del servicio.
                        <br>4.	Cerrar los streams.
                        <br>5.	Cerrar el socket.                           
                        </p>
                     </div>
                  </div>
                  <p>
                     La forma de implementar un servidor será:
                     <br>1.	Crear un objeto de la clase ServerSocket para escuchar peticiones en el puerto asignado al servicio.
                     <br>2.	Esperar solicitudes de clientes
                     <br>3.	Cuando se produce una solicitud:
                     <br>.     *	Aceptar la conexión obteniendo un objeto de la clase Socket
                     <br>.     *	Obtener las referencias al stream de entrada y al de salida al socket anterior.
                     <br>.     *	Leer datos del socket, procesarlos y enviar respuestas al cliente, escribiendo en el stream del socket.
                     <br>4.	Cerrar los streams.
                     <br>5.	Cerrar los sockets.
                  </p>
