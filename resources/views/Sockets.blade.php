                        <br>Los protocolos son un conjunto de estándares y reglas que admiten modos específicos de comunicación de datos. La comunicación orientada a la conexión se diseñó para garantizar que haya recursos disponibles según lo requiera el tipo de comunicaciones que se solicitan.
