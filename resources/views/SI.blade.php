                     <h4>Evolución de los sistemas de información.</h4>
                     <h5>1950 - 1960:<br>procesamiento de datos electrónicos, sistema de procesamiento de transacciones.</h5>
                     <p>Realizar actividades como el procesamiento de transacciones, el mantenimiento de registros y la contabilidad. SI se utilizó principalmente para el procesamiento electrónico de datos.
                        EDP se describe como el uso de computadoras para registrar, clasificar, manipular y resumir datos. También se llama procesamiento de información o procesamiento de datos automático.
                        Sistema de procesamiento de transacciones (TPS) fue el primer sistema computarizado desarrollado para procesar datos comerciales. TPS estaba principalmente dirigido al personal administrativo de una organización. El TPS inicial utilizaba datos de procesamiento por lotes que se acumularon durante un período y todas las transacciones se procesaron posteriormente.
                        TPS recopila, almacena, modifica y recupera las transacciones diarias de una organización. Por lo general, TPS informatiza o automatiza un proceso manual existente para permitir un procesamiento más rápido, un mejor servicio al cliente y costos administrativos reducidos. Ejemplos de productos de TPS son los depósitos en efectivo, el cajero automático (ATM), la orden de pago y los sistemas contables. TPS también se conoce como procesamiento de transacciones o procesamiento en tiempo real.
                     </p>
                  </div>
               </div>
               <h5>1960 - 1970:<br>Sistemas de información gerencial</h5>
                     <p>Durante esta era, el rol de SI evolucionó desde TPS hasta Management Information Systems (MSI) o sistema de información gerenciales MSI procesa los datos en informes informativos útiles y proporciona a los gerentes las herramientas para organizar evaluar y administrar de manera eficiente los departamentos dentro de una organización. MSI entrega información en forma de pantallas e informes pre especificados para respaldar la toma de decisiones empresariales. Algunos ejemplos de resultados de MSI son los sistemas de informes de tendencias de costos, análisis de ventas y rendimiento de producción.
                        Por lo general, MSI genera tres tipos básicos de información que son: Los informes detallados de información típicamente confirman las actividades de procesamiento de transacciones. Un informe de pedido detallado es un ejemplo de informe detallado.
                        <br>La información resumida establece datos en un formato que un individuo puede revisar rápido y fácilmente. Información mediante el filtrado de datos es uno de excepción. Los informes de excepciones ayudan a los gerentes a ahorrar tiempo porque no tienen que buscar en un informe detallado las excepciones.
                     </p>
               <div class="col-md-6">
                  <img src="images/Evo2.png" alt="#" />
               </div>
