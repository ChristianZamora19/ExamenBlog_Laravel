                     <h4>Elementos de un sistema de información</h4>
                     <p>Por lo general se considera que todos los SI contienen una diversidad de elementos clasificables en cinco grandes categorías:
                        <br>• Elementos financieros. Aquellos vinculados con el capital y con los activos disponibles de la organización.
                        <br>• Elementos tecnológicos.Aquellos que tienen que ver con la maquinaria especializada y la capacidad de procesamiento automatizado de la información.
                        <br>• Elementos humanos. Básicamente, personal, tanto especializado y directivo, como no especializado o común.
                        <br>• Elementos materialesSe refiere al emplazamiento del sistema, a su soporte físico y ubicación.
                        <br>• Elementos administrativos. Aquellos relacionados con los procesos, la mecánica de conducción, los permisos, informes, transacciones, etc.
                     </p>
                  </div>
               </div>
               <h3>Ejemplos de sistema de información</h3>
               <p>• Los sistemas de control de calidad. En los que se pide una retroalimentación al cliente y se evalúan los resultados estadísticamente para elaborar resultados interpretables por la gerencia.
                  <br>• Las bases de datos de una biblioteca.En donde está contenido el grueso volumen de documentos (libros, revistas, tesis, etc.) de la biblioteca, en función de ubicar y recuperar cada uno lo más rápida y precisamente posible.
                  <br>• Las hojas de cálculoEn las que se ingresa información en bruto y se la organiza de manera cuantificable para obtener directrices de conducción financiera.
               </p>
