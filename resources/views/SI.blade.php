            <h3>¿Qué es un sistema de información?</h3>
            <p>Cuando se habla de un sistema de información (SI) se refiere a un conjunto ordenado de mecanismos que tienen como fin la administración de datos y de información, de manera que puedan ser recuperados y procesados fácil y rápidamente. Todo sistema de información se compone de una serie de recursos interconectados y en interacción, dispuestos del modo más conveniente en base al propósito informativo trazado, como puede ser recabar información personal, procesar estadísticas, organizar archivos, etc.
               <br> Estos recursos pueden ser:
            <br> • Recursos humanos. Personal de variada índole y destrezas<br>
            • Datos. Cualquier tipo de información masiva que precisa de organizarse.<br>
            • Actividades. Procedimientos, pasos a seguir, estaciones de trabajo, etc<br>
            • Recursos informáticos. Aquellos determinados por la tecnología</p>
            <br>
            <h3>Tipos de sistemas de información</h3>
            <p>Desde un punto de vista empresarial u organizativo, los sistemas de información pueden clasificarse en:
               <br> • Sistemas de Procesamiento de Transacciones (TPS). También conocidos como sistemas de gestión operativa, recopilan la información pertinente a las transacciones de la organización, es decir, de su funcionamiento.
            <br> • Sistemas de Información Ejecutiva (EIS). Monitoriza las variables gerenciales de un área específica de la organización, a partir de la información interna y externa de la misma.<br>
            • Sistemas de Información Gerencial (MIS). Contemplan la información general de la organización y la comprenden como un todo.<br>
            • Sistemas de soporte de decisiones (DSS). Orientados al procesamiento de información intra y extra organizacional, para el apoyo en la conducción de la empresa.<br>
            </p>
            </div>

