                     <h4>Computadora centralizada</h4>
                     <p>Una computadora central es una computadora grande, potente y costosa, usada principalmente por una gran compañía para el procesamiento de una gran cantidad de datos; por ejemplo, para el procesamiento de transacciones bancarias.

                        La capacidad de una computadora central se define tanto por la velocidad de su CPU como por su gran memoria interna, su alta y gran capacidad de almacenamiento externo, sus resultados en los dispositivos E/S rápidos y considerables, la alta calidad de su ingeniería interna que tiene como consecuencia una alta fiabilidad y soporte técnico caro pero de alta calidad. Una computadora central puede funcionar durante años sin problemas ni interrupciones y las reparaciones del mismo pueden ser realizadas mientras está funcionando.
                     </p>
                  </div>
               </div>
               <p>Las computadoras centrales soportan miles de usuarios de manera simultánea que se conectan mediante falsos terminales. Algunas computadoras centrales pueden ejecutar o dar cobijo a muchos sistemas operativos y por lo tanto, no funcionan como una computadora sola, sino como varias computadoras virtuales. En este papel, una computadora central por sí sola puede remplazar docenas o cientos de pequeñas computadoras personales, reduciendo los costes administrativos y de gestión al tiempo que ofrece una escalabilidad y fiabilidad mucho mejor. La fiabilidad se consigue por la independencia de sus componentes internos señalada anteriormente, y la escalabilidad se logra porque los recursos físicos de la computadora pueden ser redistribuidos entre los terminales virtuales según las necesidades; esto es mucho más difícil de hacer con las computadoras personales, porque para quitar o añadir nuevos componentes físicos hay que desenchufar la computadora muchas veces y las limitaciones de dichos componentes son mucho mayores.
               </p>
